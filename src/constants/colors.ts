const colorArray: Array<Array<ColorObject>> = [
    redExtArray,
    yellowExtArray,
    greenExtArray,
    blueExtArray,
    violetExtArray,
    brownExtArray,
    blackExtArray
];

type AllColorsType = {
    [key: string]: string
}

const allColors: AllColorsType = colorArray.reduce((acc, arr) => {
    const newAcc: {[key: string]: string} = {};
    arr.forEach((c) => {
        newAcc[c.name] = c.hex;
    });
    return Object.assign({}, acc, newAcc);
}, {});

const getColorNameByHex = (hex: string) => Object.keys(allColors).find((k) => allColors[k] === hex);

export type ColorObject = {
    name: string,
    hex: string,
    rgb: Array<number>,
}

export type ColorPaletteType = {
    [key: string]: Array<ColorObject>
}

const palette: ColorPaletteType = {
    red: redExtArray,
    yellow: yellowExtArray,
    green: greenExtArray,
    blue: blueExtArray,
    violet: violetExtArray,
    brown: brownExtArray,
    black: blackExtArray,
};
export { palette, allColors, getColorNameByHex };