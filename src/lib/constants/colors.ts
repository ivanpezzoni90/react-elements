export const asideBackground = '#e8efef';

const colorArray: Array<Array<ColorObject>> = [
    redExtArray,
    yellowExtArray,
    orangeExtArray,
    greenExtArray,
    blueExtArray,
    turquoiseExtArray,
    tealExtArray,
    violetExtArray,
    pinkExtArray,
    brownExtArray,
    blackExtArray
];

type AllColorsType = {
    [key: string]: string
}

const allColors: AllColorsType = colorArray.reduce((acc, arr) => {
    const newAcc: { [key: string]: string } = {};
    arr.forEach((c) => {
        newAcc[c.name] = c.hex;
    });
    return Object.assign({}, acc, newAcc);
}, {});

const getColorNameByHex = (hex: string) => {
    return Object.keys(allColors).find((k) => allColors[k] === hex);
};

const allObjectColors = colorArray.reduce((acc, arr) => {
    const newAcc: { [key: string]: { rgb: string, hex: string } } = {};
    arr.forEach((c) => {
        newAcc[c.name] = {
            rgb: c.rgb.join(),
            hex: c.hex
        };
    });
    return Object.assign({}, acc, newAcc);
}, {});

const allRgbColors: AllColorsType = colorArray.reduce((acc, arr) => {
    const newAcc: { [key: string]: string } = {};
    arr.forEach((c) => {
        newAcc[c.name] = c.rgb.join();
    });
    return Object.assign({}, acc, newAcc);
}, {});

export type ColorObject = {
    name: string,
    hex: string,
    rgb: Array<number>,
}

export type ColorPaletteType = {
    [key: string]: Array<ColorObject>
}

const palette: ColorPaletteType = {
    red: redExtArray,
    yellow: yellowExtArray,
    orange: orangeExtArray,
    green: greenExtArray,
    blue: blueExtArray,
    turquoise: turquoiseExtArray,
    teal: tealExtArray,
    violet: violetExtArray,
    pink: pinkExtArray,
    brown: brownExtArray,
    black: blackExtArray,
};

export { palette, allColors, allRgbColors, allObjectColors, getColorNameByHex };